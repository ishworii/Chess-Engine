def evaluate_position(board: chess.Board) -> float:
    if board.is_checkmate():
        return float('inf') if not board.turn else -float('inf')
    elif board.is_stalemate() or board.is_insufficient_material() or board.can_claim_draw():
        return 0

    evaluation = 0

    for piece_type, table in PIECE_SQUARE_TABLES.items():
        for square in board.pieces(piece_type, chess.WHITE):
            evaluation += PIECE_VALUES[piece_type]
            evaluation += table[square]

        for square in board.pieces(piece_type, chess.BLACK):
            evaluation -= PIECE_VALUES[piece_type]
            evaluation -= table[chess.square_mirror(square)]

    return evaluation


def parse_and_evaluate(fen : str) -> str:
    try:
        board = chess.Board(fen=fen)
    except ValueError as e:
        return f"Invalid fen {e}"

    evaluation = evaluate_position(board)
    ascii_board = board.unicode()
    return f"{ascii_board}\n\nEvaluation: {evaluation}"



